"""Scraper service: fetch, parse, and upsert reviews from GitHub Pages.

Review repos are prefixed with "review-" in the ParallelScience org.
The GitHub Pages site uses the OpenReview-style template generated by
build_review_page.py in denario-scientists/tools/.
"""

import hashlib
import json
import logging
import os
import re
import sqlite3
import sys
from html.parser import HTMLParser

from review_browse.services.id_registry import get_or_assign_review_id

log = logging.getLogger(__name__)

GCS_BUCKET = "parallel-review"


# ---------------------------------------------------------------------------
# HTML parser for review GitHub Pages sites
# ---------------------------------------------------------------------------

class ReviewPageParser(HTMLParser):
    """Extract paper title, author, date from a review GitHub Pages site.

    The page template uses OpenReview-style classes:
      <div class="forum-title"><h2>Title</h2></div>
      <div class="forum-authors">Author</div>
      <div class="forum-meta"><span>date</span><span>time</span>...</div>
    And navbar links to the paper's Pages URL.
    """

    def __init__(self):
        super().__init__()
        self._in_tag = None
        self._in_class = None
        self._current_text = []

        self.title = ""
        self.author = ""
        self.date = ""
        self.time = ""
        self.paper_pages_url = ""

    def handle_starttag(self, tag, attrs):
        attrs_dict = dict(attrs)
        cls = attrs_dict.get("class", "")

        # Title: <h2> inside .forum-title
        if tag == "div" and "forum-title" in cls:
            self._in_class = "forum-title"
        elif tag == "h2" and self._in_class == "forum-title":
            self._in_tag = "title"
            self._current_text = []

        # Author: .forum-authors div
        elif tag == "div" and "forum-authors" in cls:
            self._in_tag = "authors"
            self._current_text = []

        # Meta bar: .forum-meta with <span> children
        elif tag == "div" and "forum-meta" in cls:
            self._in_class = "forum-meta"
        elif tag == "span" and self._in_class == "forum-meta":
            self._in_tag = "meta-span"
            self._current_text = []

        # Paper link in navbar
        elif tag == "a":
            href = attrs_dict.get("href", "")
            if "parallelscience.github.io" in href and "review" not in href:
                self.paper_pages_url = href

    def handle_endtag(self, tag):
        if tag == "h2" and self._in_tag == "title":
            self.title = "".join(self._current_text).strip()
            self._in_tag = None
        elif tag == "div" and self._in_class == "forum-title":
            self._in_class = None

        elif tag == "div" and self._in_tag == "authors":
            self.author = "".join(self._current_text).strip()
            self._in_tag = None

        elif tag == "span" and self._in_tag == "meta-span":
            text = "".join(self._current_text).strip()
            if re.search(r"\d{4}-\d{2}-\d{2}", text):
                self.date = re.search(r"\d{4}-\d{2}-\d{2}", text).group()
            elif "AOE" in text or re.search(r"\d{2}:\d{2}:\d{2}", text):
                m = re.search(r"(\d{2}:\d{2}:\d{2})", text)
                if m:
                    self.time = m.group(1)
            self._in_tag = None
        elif tag == "div" and self._in_class == "forum-meta":
            self._in_class = None

    def handle_data(self, data):
        if self._in_tag in ("title", "authors", "meta-span"):
            self._current_text.append(data)


# ---------------------------------------------------------------------------
# Fetching & parsing
# ---------------------------------------------------------------------------

def fetch_page(org: str, repo: str) -> str | None:
    """Fetch the GitHub Pages index.html for a review repo."""
    import urllib.request
    url = f"https://{org.lower()}.github.io/{repo}/"
    try:
        with urllib.request.urlopen(url, timeout=15) as resp:
            return resp.read().decode("utf-8", errors="replace")
    except Exception:
        return None


def fetch_review_md(org: str, repo: str) -> str:
    """Fetch the review.md from the docs/ directory."""
    import urllib.request
    url = f"https://{org.lower()}.github.io/{repo}/review.md"
    try:
        with urllib.request.urlopen(url, timeout=15) as resp:
            return resp.read().decode("utf-8", errors="replace")
    except Exception:
        return ""


def parse_review_md(md_text: str) -> dict:
    """Parse structured Skepthical markdown into sections."""
    sections = {}
    current_key = None
    current_lines = []

    heading_map = {
        "paper summary": "summary",
        "summary": "summary",
        "strengths": "strengths",
        "major issues": "major_issues",
        "minor issues": "minor_issues",
        "very minor issues": "very_minor_issues",
        "key statements and references": "key_statements",
        "key statements": "key_statements",
        "mathematical consistency audit": "maths_audit",
        "mathematics audit": "maths_audit",
        "numerical results audit": "numerics_audit",
        "numerics audit": "numerics_audit",
    }

    for line in md_text.split("\n"):
        m = re.match(r"^#{1,3}\s+(.+)$", line)
        if m:
            heading_text = m.group(1).strip().lower()
            heading_text = re.sub(r"^review:\s*", "", heading_text).rstrip(":")
            matched_key = heading_map.get(heading_text)
            if matched_key:
                if current_key:
                    sections[current_key] = "\n".join(current_lines).strip()
                current_key = matched_key
                current_lines = []
                continue
        if current_key is not None:
            current_lines.append(line)

    if current_key:
        sections[current_key] = "\n".join(current_lines).strip()

    return sections


def parse_page(html: str) -> dict | None:
    """Parse metadata from a review GitHub Pages site."""
    parser = ReviewPageParser()
    parser.feed(html)
    if not parser.title:
        return None

    date = parser.date
    if parser.time:
        date = f"{date} {parser.time}"

    return {
        "paper_title": parser.title,
        "paper_author": parser.author,
        "review_date": date or "",
        "paper_pages_url": parser.paper_pages_url,
    }


_SANITIZE_CACHE: dict[str, str] = {}


def _sanitize_review_math(md_text: str) -> str:
    """Sanitize math notation in review markdown using a cheap LLM.

    Caches results by content hash to avoid repeated API calls on rescrape.
    Returns original text if API key is unavailable or call fails.
    """
    if not md_text or len(md_text) < 100:
        return md_text

    content_hash = hashlib.sha256(md_text.encode()).hexdigest()[:16]
    if content_hash in _SANITIZE_CACHE:
        return _SANITIZE_CACHE[content_hash]

    api_key = os.environ.get("OPENAI_API_KEY")
    if not api_key:
        return md_text

    try:
        from openai import OpenAI

        prompt = (
            "You are a LaTeX math formatter for a scientific peer review document.\n"
            "\n"
            "Your ONLY job: wrap all mathematical expressions in $...$ (inline) or $$...$$ (display) "
            "delimiters with proper LaTeX commands, so KaTeX can render them.\n"
            "\n"
            "Rules:\n"
            "1. Convert Greek Unicode to LaTeX: σ→$\\sigma$, α→$\\alpha$, etc.\n"
            "2. Convert subscripts/superscripts: x_i→$x_i$, σ_norm→$\\sigma_{\\rm norm}$\n"
            "3. Convert operators: ≈→$\\approx$, ≤→$\\leq$, ≥→$\\geq$, ±→$\\pm$, ×→$\\times$, ∼→$\\sim$\n"
            "4. Do NOT wrap: section references, figure references, plain text, issue IDs, URLs, code blocks.\n"
            "5. Do NOT change any wording, structure, or content.\n"
            "6. If text is already in $...$ or $$...$$, leave it.\n"
            "7. Return the COMPLETE document.\n"
            "8. Preserve ALL markdown formatting.\n"
        )

        client = OpenAI(api_key=api_key)
        resp = client.responses.create(
            model="gpt-4.1",
            input=[{"role": "user", "content": [
                {"type": "input_text", "text": prompt + "\n\n=== DOCUMENT ===\n" + md_text},
            ]}],
            timeout=120,
        )

        result = getattr(resp, "output_text", None)
        if not result:
            out = getattr(resp, "output", None)
            if isinstance(out, list):
                for item in out:
                    c = getattr(item, "content", None)
                    if isinstance(c, list):
                        for part in c:
                            t = getattr(part, "text", None)
                            if isinstance(t, str) and t.strip():
                                result = t.strip()
                                break
                    if result:
                        break

        if result and len(result) >= len(md_text) * 0.5:
            _SANITIZE_CACHE[content_hash] = result
            log.info("Math sanitized for scraper (%d → %d chars)", len(md_text), len(result))
            return result
    except Exception as e:
        log.warning("Math sanitization failed in scraper: %s", e)

    return md_text


def _fetch_cost_json(org: str, repo: str) -> float | None:
    """Fetch cost.json from the review repo's Pages site."""
    import urllib.request
    url = f"https://{org.lower()}.github.io/{repo}/cost.json"
    try:
        with urllib.request.urlopen(url, timeout=5) as resp:
            data = json.loads(resp.read())
        cost = data.get("total_cost")
        return float(cost) if cost is not None else None
    except Exception:
        return None


def _fetch_scores_json(org: str, repo: str) -> dict | None:
    """Fetch scores.json from the review repo's Pages site."""
    import urllib.request
    url = f"https://{org.lower()}.github.io/{repo}/scores.json"
    try:
        with urllib.request.urlopen(url, timeout=5) as resp:
            data = json.loads(resp.read())
        if isinstance(data, dict) and "overall" in data:
            return data
        return None
    except Exception:
        return None


def _looks_like_repo_slug(title: str) -> bool:
    """Return True if title looks like a repo slug rather than a real title."""
    # Repo slugs are all lowercase with hyphens and no spaces
    return bool(re.match(r"^[a-z0-9][-a-z0-9]*$", title.strip()))


def _extract_title_from_review_md(md_text: str) -> str:
    """Extract the paper title from the first line of review.md.

    The format is: # **_Skepthical_** review: *Paper Title Here*
    """
    for line in md_text.split("\n"):
        line = line.strip()
        if not line:
            continue
        # Match: # **_Skepthical_** review: *Title*
        m = re.search(r"review:\s*\*(.+?)\*\s*$", line)
        if m:
            return m.group(1).strip()
        break
    return ""


def scrape_single_repo(org: str, repo: str) -> dict | None:
    """Fetch and parse a single review repo. Returns metadata dict or None."""
    html = fetch_page(org, repo)
    if not html:
        return None
    meta = parse_page(html)
    if not meta:
        return None

    # Fetch and parse the review markdown
    review_md = fetch_review_md(org, repo)

    # Sanitize math notation for proper rendering
    review_md = _sanitize_review_math(review_md)

    sections = parse_review_md(review_md)

    # If the HTML title is just a repo slug, try to extract the real title
    # from review.md (first line: # **_Skepthical_** review: *Real Title*)
    if _looks_like_repo_slug(meta["paper_title"]) and review_md:
        md_title = _extract_title_from_review_md(review_md)
        if md_title:
            log.info("Title from review.md for %s: %s", repo, md_title[:80])
            meta["paper_title"] = md_title

    # If the author is unknown/empty, try scraping the paper's own Pages site
    if meta.get("paper_author", "").lower() in ("unknown", ""):
        paper_url = meta.get("paper_pages_url", "")
        if paper_url:
            try:
                import urllib.request
                with urllib.request.urlopen(paper_url, timeout=10) as resp:
                    paper_html = resp.read().decode("utf-8", errors="replace")
                m = re.search(r'Author:\s*</span>\s*(.+?)<', paper_html)
                if not m:
                    m = re.search(r'Author:\s*([^<]+)', paper_html)
                if m:
                    meta["paper_author"] = m.group(1).strip()
                    log.info("Author from paper page for %s: %s", repo, meta["paper_author"])
            except Exception:
                pass

    # Fetch cost.json if available
    total_cost = _fetch_cost_json(org, repo)
    if total_cost is not None:
        meta["total_cost"] = total_cost

    # Fetch scores.json if available
    scores = _fetch_scores_json(org, repo)
    if scores is not None:
        meta["scores"] = scores

    meta["repo"] = repo
    meta["pages_url"] = f"https://{org.lower()}.github.io/{repo}/"
    meta["github_url"] = f"https://github.com/{org}/{repo}"
    meta["review_pdf_source_url"] = f"https://{org.lower()}.github.io/{repo}/review.pdf"
    meta["paper_pdf_source_url"] = f"https://{org.lower()}.github.io/{repo}/paper.pdf"
    meta["sections"] = sections

    return meta


# ---------------------------------------------------------------------------
# Content hashing
# ---------------------------------------------------------------------------

def compute_content_hash(meta: dict) -> str:
    sections = meta.get("sections", {})
    payload = json.dumps({
        "paper_title": meta["paper_title"],
        "paper_author": meta["paper_author"],
        "summary": sections.get("summary", ""),
        "strengths": sections.get("strengths", ""),
        "major_issues": sections.get("major_issues", ""),
    }, sort_keys=True)
    return hashlib.sha256(payload.encode()).hexdigest()


# ---------------------------------------------------------------------------
# PDF handling
# ---------------------------------------------------------------------------

def download_pdf(
    pdf_source_url: str, review_id: str, version: int,
    prefix: str = "review",
    gcs_bucket: str | None = None,
) -> str | None:
    import urllib.request
    try:
        with urllib.request.urlopen(pdf_source_url, timeout=30) as resp:
            pdf_data = resp.read()
    except Exception as exc:
        log.error("PDF download failed for %s: %s", pdf_source_url, exc)
        return None
    if not pdf_data:
        return None

    # Sanitize review_id for filename (replace : and / with _)
    safe_id = review_id.replace(":", "_").replace("/", "_")
    filename = f"{prefix}_{safe_id}v{version}.pdf"

    if gcs_bucket:
        try:
            from google.cloud import storage
            client = storage.Client()
            bucket = client.bucket(gcs_bucket)
            blob = bucket.blob(filename)
            blob.upload_from_string(pdf_data, content_type="application/pdf")
            return f"https://storage.googleapis.com/{gcs_bucket}/{filename}"
        except Exception as exc:
            log.error("GCS PDF upload failed: %s", exc)
    return None


