# Upsert logic
# ---------------------------------------------------------------------------

def upsert_review(
    conn: sqlite3.Connection,
    meta: dict,
    org: str,
    gcs_bucket: str | None = GCS_BUCKET,
    skip_pdf: bool = False,
) -> tuple[str, int, str]:
    """Insert or update a review. Returns (review_id, version, action)."""
    repo = meta["repo"]
    content_hash = compute_content_hash(meta)
    sections = meta.get("sections", {})

    # Derive paper repo from review repo: "review-{paper_repo}" → "{paper_repo}"
    paper_repo = repo[len("review-"):] if repo.startswith("review-") else repo

    review_id = get_or_assign_review_id(conn, paper_repo=paper_repo, review_repo=repo, org=org)

    existing = conn.execute(
        "SELECT version, content_hash FROM reviews "
        "WHERE review_id = ? AND is_current = 1",
        (review_id,),
    ).fetchone()

    if existing and existing["content_hash"] == content_hash:
        # Even if content is unchanged, update scores if they're newly available
        new_scores = meta.get("scores") or {}
        if new_scores.get("overall") is not None:
            current_score = conn.execute(
                "SELECT score_overall FROM reviews WHERE review_id = ? AND is_current = 1",
                (review_id,),
            ).fetchone()
            if current_score and current_score["score_overall"] is None:
                conn.execute(
                    "UPDATE reviews SET score_overall=?, score_soundness=?, score_novelty=?, "
                    "score_significance=?, score_clarity=?, score_evidence=?, score_justification=? "
                    "WHERE review_id=? AND is_current=1",
                    (
                        new_scores.get("overall"),
                        new_scores.get("soundness"),
                        new_scores.get("novelty"),
                        new_scores.get("significance"),
                        new_scores.get("clarity"),
                        new_scores.get("evidence_quality"),
                        new_scores.get("justification", ""),
                        review_id,
                    ),
                )
                conn.commit()
                log.info("SCORES %s (overall=%s)", review_id, new_scores.get("overall"))
                return review_id, existing["version"], "scores_updated"
        return review_id, existing["version"], "unchanged"

    if existing:
        old_version = existing["version"]
        new_version = old_version + 1
        conn.execute(
            "UPDATE reviews SET is_current = 0 WHERE review_id = ? AND version = ?",
            (review_id, old_version),
        )
        action = "updated"
    else:
        new_version = 1
        action = "new"

    review_pdf_url = ""
    paper_pdf_url = ""
    if not skip_pdf:
        review_pdf_url = download_pdf(
            meta["review_pdf_source_url"], review_id, new_version,
            prefix="review", gcs_bucket=gcs_bucket,
        ) or ""
        paper_pdf_url = download_pdf(
            meta["paper_pdf_source_url"], review_id, new_version,
            prefix="paper", gcs_bucket=gcs_bucket,
        ) or ""

    # Extract PX ID from the review_id (e.g. "2604.00003-R1" → "2604.00003")
    px_id = review_id.rsplit("-R", 1)[0] if "-R" in review_id else ""

    scores = meta.get("scores") or {}

    conn.execute(
        "INSERT INTO reviews "
        "(review_id, version, paper_title, paper_author, review_date, "
        " summary, strengths, major_issues, minor_issues, very_minor_issues, "
        " maths_audit, numerics_audit, reviewer, repo, "
        " pages_url, github_url, review_pdf_url, paper_pdf_url, "
        " paper_pages_url, px_id, total_cost, "
        " score_overall, score_soundness, score_novelty, score_significance, "
        " score_clarity, score_evidence, score_justification, "
        " is_current, content_hash) "
        "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 1, ?)",
        (
            review_id, new_version,
            meta["paper_title"], meta["paper_author"], meta["review_date"],
            sections.get("summary", ""),
            sections.get("strengths", ""),
            sections.get("major_issues", ""),
            sections.get("minor_issues", ""),
            sections.get("very_minor_issues", ""),
            sections.get("maths_audit", ""),
            sections.get("numerics_audit", ""),
            "Skepthical",
            repo,
            meta["pages_url"], meta["github_url"],
            review_pdf_url, paper_pdf_url,
            meta.get("paper_pages_url", ""),
            px_id,
            meta.get("total_cost"),
            scores.get("overall"),
            scores.get("soundness"),
            scores.get("novelty"),
            scores.get("significance"),
            scores.get("clarity"),
            scores.get("evidence_quality"),
            scores.get("justification", ""),
            content_hash,
        ),
    )
    conn.commit()

    log.info("%s %s v%d (%s)", action.upper(), review_id, new_version, meta["paper_title"][:60])
    return review_id, new_version, action


